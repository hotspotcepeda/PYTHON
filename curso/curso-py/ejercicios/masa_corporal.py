# El índice de masa corporal (IMC) fue creado por un matemático belga en la década de 1850 y lo utilizan los 
# profesionales de la salud y la nutrición para estimar la grasa corporal humana en determinadas poblaciones.
# Se calcula tomando el peso (masa) de un individuo en kilogramos y dividiéndolo por el cuadrado de su altura en metros.

peso_kilos = 90
altura_metros = 2

imc = (peso_kilos / (altura_metros ** 2))

print("su IMC es: ", imc)

